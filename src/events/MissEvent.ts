import type { NDTObjectKind } from './types';

/**
 * Событие упущенного объекта (вышел за нижний край экрана).
 *
 * Контракт зафиксирован в плане (фаза 2/4). НЕ менять поля, только расширять
 * потребителей.
 *
 * Эмитент: SpawnDirector (деспавнер) в фазе 2.
 * Потребитель: LifeSystem в фазе 4 — для режимых объектов уменьшает жизни;
 *               для трубы-бомбы (isBomb=true) штрафа нет (мина, не цель).
 *
 * В фазе 2 консамер — заглушка-логгер в GameScene. LifeSystem подключается в фазе 4.
 */
export interface MissEvent {
  /** id Matter-тела упущенного объекта. */
  readonly bodyId: number;
  /** Тип объекта. */
  readonly kind: NDTObjectKind;
  /**
   * true только для 'pipe'. Труба-бомба, упущенная за нижний край, штрафа не
   * несёт (мина, не цель) — но событие всё равно эмитится, чтобы консамер сам
   * решал, штрафовать или нет.
   */
  readonly isBomb: boolean;
}
